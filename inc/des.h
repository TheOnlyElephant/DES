static uint64_t permute(uint64_t input, int input_size, const int *table, int n) {
    uint64_t output = 0;

    for (int i = 0; i < n; i++) {
        int from = table[i] - 1; // 表中的位置从 1 开始
        uint64_t bit = (input >> (input_size - from - 1)) & 1ULL;
        output |= bit << (n - i - 1);
    }

    return output;
}

// Permutation optimized for 64-bit input
static uint64_t permute_64(uint64_t input, const int *table, int n) {
    uint64_t output = 0;

    // Perform permutation using bit manipulation
    for (int i = 0; i < n; i++) {
        int from = table[i] - 1; // 1-based index in the table
        uint64_t bit = (input >> (63 - from)) & 1ULL;
        output |= bit << (n - i - 1);
    }

    return output;
}


// Left rotate
static void rotate_left(unsigned char *key, int n, int bits) {
    unsigned char temp = 0;
    for (int i = 0; i < bits; i++) {
        temp = (key[0] >> 7) & 1; // Save the leftmost bit
        for (int j = 0; j < n; j++) {
            key[j] = (key[j] << 1) | ((key[j + 1] >> 7) & 1);
        }
        key[n - 1] = (key[n - 1] << 1) | temp; // Restore the saved bit
    }
}
